     * @param  \App\Http\Requests\ThreadRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(ThreadRequest $request)
    {
        $thread = auth()->user()->threads()->create($request->all());

        return redirect($thread->path());
    }

